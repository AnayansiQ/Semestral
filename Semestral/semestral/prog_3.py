def program_3():
    
    # Area de un triangulo
    
    print('+-----------------------------------------+')
    print("|Hallar el area de un triangulo rectangulo|")
    print('+-----------------------------------------+ \n')
    
    Base = float(input("La base del triangulo es: "))
    Altura = float(input("La altura del triangulo es: "))
    
    Area = (Base * Altura) / 2
    
    print(" \nRespuesta del programa #3")
    print("=========================\n")
    
    print(f"El area de un triangulo rectangulo es: {Area}")
    
    print("\n")
    input("Press the enter key to continue: ")